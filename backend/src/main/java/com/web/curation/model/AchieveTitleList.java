package com.web.curation.model;

public class AchieveTitleList {

	String id;
	String title;
	Integer clear_cnt;
	Integer achieve_cnt;
	Integer percent;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public Integer getClear_cnt() {
		return clear_cnt;
	}
	public void setClear_cnt(Integer clear_cnt) {
		this.clear_cnt = clear_cnt;
	}
	public Integer getAchieve_cnt() {
		return achieve_cnt;
	}
	public void setAchieve_cnt(Integer achieve_cnt) {
		this.achieve_cnt = achieve_cnt;
	}
	public Integer getPercent() {
		return percent;
	}
	public void setPercent(Integer percent) {
		this.percent = percent;
	}
	
	
}
